package Day6;

import java.util.Scanner;

public class ContinueDemo {
    public static void main(String[] args)
    {
        int x = 10;

        for  (int i =0; i <x; i++)
        {
            System.out.println(i);
            if (i == 7);
            {
                continue;

            }

        }
        System.out.println("--------------");
    }
}