package Day3;

public class OperatorDemo4 {
    public static void main (String [] agrs){
      //4. Relational Operator
      // >< >= <= == !=
        int a = 5;
        int b = 5;
        System.out.println(a < b);
        System.out.println(a > b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a == b);
        System.out.println(a != b);


    }
}
