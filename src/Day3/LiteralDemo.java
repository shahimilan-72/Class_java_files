package Day3;

public class LiteralDemo {
    //Literals: fixed value +d datatype

    //1. Integer Literal 43543 67657 335 ( default int)
     byte b = 127;
     short s = 32767;
     int i = 2147483647;
     long l1 = 2147483647;
     long l2 = 2147483648L;
     long l3 = 9223372036854775807L;

     //2. Floating Literal 45.67 2343.56 (default double)
    float f = 45.76F;
    double d = 45.76;

    //3. Boolean Literal true or false
    boolean b1 = true;
    boolean b2 = false;

    // Character Literal 'a' 1' '?'
    char c1 = 'a';
    char c2 = '1';
    char c3 = '#';
    char c4 = '\u0000';

    //5. String Literal 'saf' " "
    String s1 = "ram";
    String s2 = "abc123!#";
    String s3 = "";
    String s4 = "  ";
    String s5 = "34.46";
    String s6 = "556";
    String s7 = "true";
    String s8 = "Hello ! My name is Milan Shahi";
    String s9 = "2011-01-11";

    //6. Null Literals
    String s10 = null; //this is object in java




}
// By default literal type is integer
